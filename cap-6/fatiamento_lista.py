L = [1,2,3,4,5]

print(f"Índices: {list(range(len(L)))}") #Mostra os índices da lista

print(f"L[0:5] -> {L[0:5]}") #Elementos entre a posição 0 e 4 (o 5 fica de fora)

print(f"L[:5] -> {L[:5]}") #Primeiros 5 elementos

print(f"L[:-1] -> {L[:-1]}") #Todos os elementos, exceto o último

print(f"L[1:3] -> {L[1:3]}") #Elementos entre a posição 1 e 2 (o 3 fica de fora)

print(f"L[1:4] -> {L[1:4]}") #Elementos entre a posição 1 e 3 (o 4 fica de fora)

print(f"L[3:] -> {L[3:]}") #Elementos a partir do 3 até o fim da lista

print(f"L[:3] -> {L[:3]}") #Elementos até a posição 3 (0,1,2)

print(f"L[-1] -> {L[-1]}") #Último elemento (pos 4)

print(f"L[-2] -> {L[-2]}") #Penúltimo elemento (pos 3)